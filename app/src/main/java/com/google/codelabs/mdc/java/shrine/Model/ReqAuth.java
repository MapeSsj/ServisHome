package com.google.codelabs.mdc.java.shrine.Model;

public class ReqAuth {


    public void setUsername(String username) {
        this.username = username;
    }

    public void setClaveUsuario(String claveUsuario) {
        this.claveUsuario = claveUsuario;
    }

    public void setTipo_usuario(String tipo_usuario) {
        this.tipo_usuario = tipo_usuario;
    }

    public void setFecha_registro(String fecha_registro) {
        this.fecha_registro = fecha_registro;
    }

    public void setNumDocumento(String numDocumento) {
        this.numDocumento = numDocumento;
    }

    public void setImgUsuario(String imgUsuario) {
        this.imgUsuario = imgUsuario;
    }

    public void setEmailUsuario(String emailUsuario) {
        this.emailUsuario = emailUsuario;
    }

    public void setTipoUsuario(String tipoUsuario) {
        this.tipoUsuario = tipoUsuario;
    }

    public void setTokenUuario(String tokenUuario) {
        this.tokenUuario = tokenUuario;
    }

    public void setEstadoTokenUsuario(String estadoTokenUsuario) {
        this.estadoTokenUsuario = estadoTokenUsuario;
    }

    private String username;
    private String claveUsuario;

    private String tipo_usuario;

    private String fecha_registro;

    private String numDocumento;

    private String imgUsuario;

    private String emailUsuario;

    private String tipoUsuario;

    private String tokenUuario;

    private String estadoTokenUsuario;

}
